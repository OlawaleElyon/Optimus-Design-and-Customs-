// Main handler
module.exports = async (req, res) => {
  // Set CORS headers
  res.setHeader('Access-Control-Allow-Credentials', true);
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET,OPTIONS,PATCH,DELETE,POST,PUT');
  res.setHeader('Access-Control-Allow-Headers', 'X-CSRF-Token, X-Requested-With, Accept, Accept-Version, Content-Length, Content-MD5, Content-Type, Date, X-Api-Version');
  
  // Handle preflight
  if (req.method === 'OPTIONS') {
    res.status(200).end();
    return;
  }
  
  // Only allow POST
  if (req.method !== 'POST') {
    return res.status(405).json({ 
      status: 'error',
      message: 'Method not allowed. Use POST.' 
    });
  }
  
  const requestId = Date.now();
  console.log(`\n${'='.repeat(80)}`);
  console.log(`üìã NEW APPOINTMENT REQUEST [${requestId}]`);
  console.log(`${'='.repeat(80)}`);
  
  try {
    const data = req.body;
    
    // Validate input
    const validationErrors = validateInput(data);
    if (validationErrors.length > 0) {
      console.log('‚ùå Validation failed:', validationErrors);
      return res.status(400).json({
        status: 'error',
        message: 'Validation failed',
        errors: validationErrors
      });
    }
    
    console.log(`   Customer: ${data.name}`);
    console.log(`   Email: ${data.email}`);
    console.log(`   Service: ${data.serviceType}`);
    
    // STEP 1: SAVE TO SUPABASE (CRITICAL - MUST SUCCEED)
    let appointmentId;
    try {
      console.log('üíæ Saving to Supabase...');
      
      const supabase = getSupabaseClient();
      
      const dbData = {
        name: data.name,
        email: data.email,
        phone: data.phone,
        service_type: data.serviceType,
        preferred_date: data.preferredDate,
        project_details: data.message || ''
      };
      
      const { data: insertData, error: insertError } = await supabase
        .from('appointments')
        .insert([dbData])
        .select();
      
      if (insertError) {
        throw new Error(`Supabase error: ${insertError.message}`);
      }
      
      if (!insertData || insertData.length === 0) {
        throw new Error('No data returned from Supabase');
      }
      
      appointmentId = insertData[0].id;
      console.log(`‚úÖ Saved to Supabase: ${appointmentId}`);
      
    } catch (dbError) {
      console.error('‚ùå CRITICAL: Database save failed:', dbError);
      return res.status(500).json({
        status: 'error',
        message: 'Failed to save appointment. Please try again or contact us directly.',
        details: process.env.NODE_ENV === 'development' ? dbError.message : undefined
      });
    }
    
    // STEP 2: SEND EMAIL VIA RESEND (BEST EFFORT)
    let emailSent = false;
    try {
      const resend = getResendClient();
      
      if (resend) {
        console.log('üìß Sending email via Resend...');
        
        const senderEmail = process.env.RESEND_SENDER_EMAIL || 'onboarding@resend.dev';
        const recipientEmail = process.env.RECIPIENT_EMAIL || 'elyonolawale@gmail.com';
        
        const emailHtml = createEmailHtml(data, appointmentId);
        
        const { data: emailData, error: emailError } = await resend.emails.send({
          from: `Optimus Design & Customs <${senderEmail}>`,
          to: [recipientEmail],
          subject: `New Appointment Request - ${data.serviceType}`,
          html: emailHtml,
          replyTo: data.email
        });
        
        if (emailError) {
          throw new Error(emailError.message);
        }
        
        console.log(`‚úÖ Email sent successfully: ${emailData.id}`);
        emailSent = true;
        
      } else {
        console.warn('‚ö†Ô∏è  Resend not configured, skipping email');
      }
      
    } catch (emailError) {
      console.error('‚ùå Email sending failed:', emailError.message);
      
      // STEP 3: LOG TO FALLBACK SYSTEM
      try {
        console.log('üìù Logging to email fallback system...');
        
        const supabase = getSupabaseClient();
        const recipientEmail = process.env.RECIPIENT_EMAIL || 'elyonolawale@gmail.com';
        const emailHtml = createEmailHtml(data, appointmentId);
        
        await supabase.from('email_notifications').insert([{
          appointment_id: appointmentId,
          recipient_email: recipientEmail,
          subject: `New Appointment Request - ${data.serviceType}`,
          body: emailHtml,
          status: 'pending',
          attempts: 0,
          error_message: emailError.message.substring(0, 500)
        }]);
        
        console.log('‚úÖ Logged to fallback system');
        
      } catch (fallbackError) {
        console.error('‚ùå Fallback logging failed:', fallbackError.message);
        console.warn(`‚ö†Ô∏è  MANUAL ACTION: Check Supabase appointments table for ID: ${appointmentId}`);
      }
    }
    
    // ALWAYS RETURN SUCCESS IF SAVED TO DATABASE
    console.log(`${'='.repeat(80)}`);
    console.log(`‚úÖ APPOINTMENT COMPLETED [${requestId}]`);
    console.log(`   ID: ${appointmentId}`);
    console.log(`   Email Sent: ${emailSent}`);
    console.log(`${'='.repeat(80)}\n`);
    
    return res.status(200).json({
      status: 'success',
      message: 'Your request has been submitted successfully. We\'ll contact you shortly!',
      appointment_id: appointmentId,
      email_sent: emailSent
    });
    
  } catch (error) {
    console.error('‚ùå UNEXPECTED ERROR:', error);
    return res.status(500).json({
      status: 'error',
      message: 'An unexpected error occurred. Please try again or contact us directly.',
      details: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  }
};
