// Main handler
module.exports = async (req, res) => {
  // Set CORS headers
  res.setHeader('Access-Control-Allow-Credentials', true);
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET,OPTIONS,PATCH,DELETE,POST,PUT');
  res.setHeader('Access-Control-Allow-Headers', 'X-CSRF-Token, X-Requested-With, Accept, Accept-Version, Content-Length, Content-MD5, Content-Type, Date, X-Api-Version');
  
  // Handle preflight
  if (req.method === 'OPTIONS') {
    res.status(200).end();
    return;
  }
  
  // Only allow POST
  if (req.method !== 'POST') {
    return res.status(405).json({ 
      status: 'error',
      message: 'Method not allowed. Use POST.' 
    });
  }
  
  console.log('üìã NEW APPOINTMENT REQUEST');
  
  try {
    const data = req.body;
    
    // Validate input
    const validationErrors = validateInput(data);
    if (validationErrors.length > 0) {
      console.log('‚ùå Validation failed:', validationErrors);
      return res.status(400).json({
        status: 'error',
        message: 'Validation failed',
        errors: validationErrors
      });
    }
    
    console.log(`Customer: ${data.name}, Email: ${data.email}, Service: ${data.serviceType}`);
    
    // STEP 1: SEND EMAIL VIA RESEND (PRIMARY)
    let emailSent = false;
    let emailError = null;
    
    try {
      const resend = getResendClient();
      
      if (!resend) {
        throw new Error('Resend API key not configured');
      }
      
      console.log('üìß Sending email via Resend...');
      
      const recipientEmail = process.env.RECIPIENT_EMAIL || 'elyonolawale@gmail.com';
      const senderEmail = process.env.RESEND_SENDER_EMAIL || 'onboarding@resend.dev';
      
      const emailHtml = createEmailHtml(data);
      
      const { data: emailData, error: sendError } = await resend.emails.send({
        from: `Optimus Design & Customs <${senderEmail}>`,
        to: [recipientEmail],
        subject: `New Appointment Request - ${data.serviceType}`,
        html: emailHtml,
        reply_to: data.email
      });
      
      if (sendError) {
        throw new Error(sendError.message);
      }
      
      console.log(`‚úÖ Email sent successfully: ${emailData.id}`);
      emailSent = true;
      
    } catch (err) {
      console.error('‚ùå Email sending failed:', err.message);
      emailError = err.message;
    }
    
    // STEP 2: SAVE TO SUPABASE (OPTIONAL)
    let appointmentId = null;
    
    try {
      const supabase = getSupabaseClient();
      
      if (supabase) {
        console.log('üíæ Saving to Supabase...');
        
        const dbData = {
          name: data.name,
          email: data.email,
          phone: data.phone,
          service_type: data.serviceType,
          preferred_date: data.preferredDate,
          project_details: data.message || '',
          email_sent: emailSent
        };
        
        const { data: insertData, error: insertError } = await supabase
          .from('appointments')
          .insert([dbData])
          .select();
        
        if (insertError) {
          console.error('Supabase error:', insertError.message);
        } else if (insertData && insertData.length > 0) {
          appointmentId = insertData[0].id;
          console.log(`‚úÖ Saved to Supabase: ${appointmentId}`);
        }
      }
    } catch (dbError) {
      console.error('Database save failed:', dbError.message);
    }
    
    // Return response based on email status
    if (emailSent) {
      return res.status(200).json({
        status: 'success',
        message: "Your request has been submitted successfully. We'll contact you shortly!",
        appointment_id: appointmentId,
        email_sent: true
      });
    } else {
      // Email failed - return error
      return res.status(500).json({
        status: 'error',
        message: 'Failed to send your request. Please try again or contact us directly.',
        error: emailError
      });
    }
    
  } catch (error) {
    console.error('‚ùå UNEXPECTED ERROR:', error);
    return res.status(500).json({
      status: 'error',
      message: 'An unexpected error occurred. Please try again or contact us directly.'
    });
  }
};
